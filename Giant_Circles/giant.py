# Giant Circles

import turtle

def main():    
    turtle.color("green")
    turtle.dot(500)
    turtle.color("blue")
    turtle.dot(400)
    turtle.color("yellow")
    turtle.dot(300)
    turtle.color("red")
    turtle.dot(200)
    turtle.color("purple")
    turtle.dot(100)
    
    turtle.done()


if __name__ == "__main__":
    main()
